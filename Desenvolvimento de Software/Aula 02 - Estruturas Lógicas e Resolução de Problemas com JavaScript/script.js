// estruturas condicionais
// == (igual a)
// === (estritamente igual a)
// >, < (maior que, menor que)
// >=, <= (maior ou igual, menor ou igual)
// != (diferente de)
// !== (estritamente diferente de)

console.log(`"1" == 1: ${"1" == 1}`);
console.log(`"1" === 1: ${"1" === 1}`);
console.log(`20 > 20: ${20 > 20}`);
console.log(`20 <= 20: ${20 <= 20}`);
console.log(`1 != 2: ${1 != 2}`);
console.log(`1 !== "1": ${1 !== "1"}`);
